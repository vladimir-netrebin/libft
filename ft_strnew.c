#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	if (!(s = (char*)malloc(size + 1)))
		return (NULL);
	s[size] = '\0';
	while (size > 0)
	{
		s[size] = '\0';
		size--;
	}
	s[0] = '\0';
	return (s);
}
