char	*ft_strcpy(char *dst, const char *src)
{
	char	*c;
	char	*s;

	s = dst;
	c = (char*)src;
	while (*c != '\0')
	{
		*dst = *c;
		dst++;
		c++;
	}
	*dst = *c;
	return (s);
}
