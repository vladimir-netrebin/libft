#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*i;
	char	*s1;

	s1 = NULL;
	i = (char*)s;
	while (*i != '\0')
	{
		if (*i == c)
			s1 = i;
		i++;
	}
	if (c == '\0')
		return (i);
	return (s1);
}
