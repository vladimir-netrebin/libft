#include "libft.h"

int	ft_in(char *c, char *s)
{
	while (*c != '\0' && *s != '\0')
	{
		if (*c != *s)
			return (0);
		c++;
		s++;
	}
	if (*s == '\0')
		return (1);
	return (0);
}
