#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*i;

	i = (char*)s;
	while (*i != '\0')
	{
		if (*i == c)
			return (i);
		i++;
	}
	if (c == '\0')
		return (i);
	return (NULL);
}
