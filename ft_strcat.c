#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*c;
	char	*c2;

	c = (char*)s2;
	c2 = s1;
	while (*s1 != '\0')
		s1++;
	while (*c != '\0')
	{
		*s1 = *c;
		s1++;
		c++;
	}
	*s1 = '\0';
	return (c2);
}
