#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c;
	char	*c2;

	c = (char*)s2;
	c2 = s1;
	while (*s1 != '\0')
		s1++;
	while (*c != '\0' && n > 0)
	{
		*s1 = *c;
		s1++;
		c++;
		n--;
	}
	*s1 = '\0';
	return (c2);
}
